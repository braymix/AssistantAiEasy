def update_config(dry_run: bool = False) -> dict[str, Any]:
    """Generate and write the enterprise configuration.

    Returns a summary dict with paths and changes made.
    """
    logger.info("=" * 60)
    logger.info("KnowledgeHub — Configuration Update (mini → enterprise)")
    if dry_run:
        logger.info("MODE: DRY RUN")
    logger.info("=" * 60)

    env_path = Path(".env")
    result: dict[str, Any] = {"changes": []}

    # Read current
    current = _read_current_env(str(env_path))
    logger.info("Current profile: %s", current.get("KNOWLEDGEHUB_PROFILE", "mini"))

    # Generate new .env
    new_env_content = generate_enterprise_env(current)

    if dry_run:
        logger.info("Generated .env preview (first 40 lines):")
        for line in new_env_content.splitlines()[:40]:
            logger.info("  %s", line)
        result["dry_run"] = True
        return result

    # Backup current .env
    if env_path.exists():
        backup_name = f".env.backup.{datetime.now(timezone.utc).strftime('%Y%m%d_%H%M%S')}"
        shutil.copy2(env_path, backup_name)
        logger.info("Backed up current .env → %s", backup_name)
        result["changes"].append(f"Backed up .env to {backup_name}")

    # Write new .env
    env_path.write_text(new_env_content)
    logger.info("Written new enterprise .env")
    result["changes"].append("Written new .env with profile=full")

    # Create docker-compose.override.yml to use enterprise compose
    override_path = Path("docker-compose.override.yml")
    if not override_path.exists():
        override_content = (
            "# Auto-generated by migration script\n"
            "# This file extends docker-compose.yml with enterprise services.\n"
            "# To use: docker compose -f docker-compose.yml "
            "-f docker-compose.enterprise.yml up -d\n"
        )
        override_path.write_text(override_content)
        logger.info("Created docker-compose.override.yml")
        result["changes"].append("Created docker-compose.override.yml")

    logger.info("-" * 60)
    logger.info("Configuration update COMPLETE")
    logger.info("Next: start enterprise services with:")
    logger.info("  docker compose -f docker-compose.yml -f docker-compose.enterprise.yml up -d")

    result["env_path"] = str(env_path)
    return result


def main() -> None:
    dry_run = "--dry-run" in sys.argv
    update_config(dry_run=dry_run)


if __name__ == "__main__":
    main()
