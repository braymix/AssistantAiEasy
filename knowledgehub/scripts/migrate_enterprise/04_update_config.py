def _read_current_env(env_path: str = ".env") -> dict[str, str]:
    """Parse the current .env file into a dict."""
    values: dict[str, str] = {}
    p = Path(env_path)
    if not p.exists():
        return values

    for line in p.read_text().splitlines():
        line = line.strip()
        if not line or line.startswith("#"):
            continue
        if "=" in line:
            key, _, val = line.partition("=")
            values[key.strip()] = val.strip()

    return values


def _get_value(
    current: dict[str, str], key: str, default: str,
) -> str:
    """Get a value from current env or environment, with fallback."""
    env_val = os.environ.get(key, "")
    cur_val = current.get(key, "")
    return env_val or cur_val or default


